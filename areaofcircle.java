import java.util.*;

public class areaofcircle {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("ENTER RADIUS");
        int r=sc.nextInt();
        double pi=3.14;
        double a=pi*r*r;
        System.out.println("AREA:"+a);
    }
    
}
